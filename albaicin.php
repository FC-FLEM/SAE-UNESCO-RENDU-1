                    La ville médiévale est caractérisée par ses petites rues sinueuses et ses placettes, bordées de modestes habitations de styles andalou et mauresque. L'étroitesse des ruelles et la hauteur des maisons permettent de garder la fraîcheur, même par temps chaud en Andalousie. <br>
