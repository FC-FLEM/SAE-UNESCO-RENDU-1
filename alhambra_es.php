        <h3>Historia de la Alhambra</h3>
                <div class="Histoire">
                <img class="histoire" src="./img/Grenade/Night_view_Alhambra.jpg" float="left">
                <p>
                    La Alhambra tiene lugar en la colina de la Sabika durante el <b>IXe</b> siglo. Fue un antiguo primer ministro llamado Samuel ibn Nagrella quien reconstruyó los edificios abandonados de esta colina e instaló aquí su palacio. 
                </p><p>Al <b>XIIe</b> siglo, estos edificios sirven de refugio durante las batallas en la Alcazaba del Albaicín.
                </p><p>Fue en <b>1238</b> cuando el fundador de la dinastía nazarí, Al-Ahmar, se instaló en la antigua Alcazaba del Albaicín. Decidió reconstruir las ruinas de la colina, edificando así la actual Alhambra. Fue un palacio, una ciudadela y una fortaleza, pero, sobre todo, una residencia de los altos funcionarios nazaríes. Granada se convirtió después en la capital del reino nazarí, expandiéndose con el tiempo para dar lugar a nuevos barrios hasta finales del siglo XV.
                </p><p>Después de 1492, la Alhambra se convirtió en Casa Real.
                </p><p>El emperador Carlos V decidió en 1526 construir su palacio homónimo, así como otros edificios de estilo renacentista romano.
                </p><p>Al <b>XIXe</b> siglo, la sociedad se interesó mucho por los jardines de la Alhambra. Después de la revolución de 1868, la Alhambra se separó de la Corona y pasó a ser dominio del Estado, siendo declarada "monumento nacional" en 1870. <br>Actualmente, el cuidado de la Alhambra está encomendado a la Comunidad Autónoma de Andalucía, responsable de las funciones y servicios del Estado en materia de cultura.</p>
                <br>
