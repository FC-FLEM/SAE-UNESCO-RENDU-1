            <h3>Arte y arquitectura</h3>
                <div class="ART">
                <img class="arte" src="./img/Grenade/Arquitectura_Alhambra.jpg" float="left">
                <p>Fue en el siglo XIV cuando la Alhambra se embelleció con decoraciones con múltiples símbolos musulmanes, en particular por obra de Yusuf I. En la Puerta de la Ley Islámica se administraba la justicia de Granada, lo que convierte a la Alhambra en algo más que un edificio defensivo o decorativo. Su arquitectura defensiva hace de la Alhambra una fortaleza (murallas, puertas fortificadas, torres de vigilancia) con una estética "oriental" que utilizaban los emires que residían en el Generalife, situado en la Alhamlbra. <br>Los últimos edificios tienen muchos arcos circulares y tejados abovedados para dejar entrar toda la luz posible, un tema importante en la arquitectura musulmana.</p>
                <p>Granada sigue el modelo histórico de ciudad de montaña, rodeada de montañas y regada por los ríos Darro y Genil.
                    Rodeada de montañas, los escritores árabes comparaban Granada con una corona, en cuyo frente se alza la <i>"diadema de la Alhambra"</i>.</p>
