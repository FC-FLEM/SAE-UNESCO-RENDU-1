                            <h4>Alcabaza</h4>
                            <p>La Alcazaba se considera la parte más antigua de la Alhambra. De hecho, fue a principios del siglo IX cuando se informó de construcciones en la colina de La Sabika. Más tarde, en este mismo lugar se construyó la majestuosa Alhambra.
                                Originalmente, era una fortaleza construida con el único propósito de proteger la ciudad de Granada de las invasiones enemigas.

                            </p>
                            <p>El edificio está dividido en dos partes: la superior y la inferior. La parte superior o ciudadela se utilizaba como residencia de los gobernantes moros y su corte, mientras que la parte inferior servía de cuartel militar. </p>
                            <p>La Alcazaba ha sido restaurada varias veces a lo largo de los siglos, especialmente por los cristianos, y ha sido utilizada como prisión estatal en diferentes épocas, incluso durante la ocupación franquista.</p>
