                            <h4>Palais de Charles Quint</h4>
                            <p>Le palais de Carlos V est un palais construit sur les ordres de l'empereur Charles Quint, celui-ci ayant fait de l'Alhambra sa résidence d'été.
                                La construction de son palais a débuté au XVIe siècle, mais n'a jamais été achevée et est donc resté inhabité.</p>
                            <p>Ce palais carré se situe à proximité des Palais Nasrides et coupe les jardins reliant l'entrée de l'Alhambra et l'Alcazaba. Il s'agit de l'œuvre la plus importante de la Renaissance en Espagne avec son patio circulaire central unique dans le style.</p>
                            <p>Jadis, le bâtiment était relié à l'Alcazaba de l'Alhambra. On peut le remarquer par ses côtés sud et ouest entièrement décorés par rapport à son nord et est.</p>
