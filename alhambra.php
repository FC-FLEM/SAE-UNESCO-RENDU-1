                                    <h4><b>Jardin de Partal</b></h4>
                                    <p>Son nom fait référence à un portique, puisqu'il fait la liaison entre une myriade de bâtiments de l'Alhambra (Il est entouré de bâtiments importants de l'Alhambra, tels que le Palais de Comares et le Palais des Lions).
                                        Le Jardin de Partal est connu pour ses fontaines et ses bassins, conçus afin de refroidir l'air et créer une atmosphère agréable pour les visiteurs. En son sein, on peut retrouver beaucoup de végétation : toutes sortes de plantes et d'arbres tels que des oliviers, des cyprès et des orangers.
                                        Le jardin donne sur la Torre de la Dama, un bâtiment connu pour abriter les marchands et magnats de l'époque qui venaient pour se détendre et saluer la royauté.
                                    </p>
