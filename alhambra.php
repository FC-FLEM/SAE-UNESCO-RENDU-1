                                    <h4><b>Cour des Myrtes</b></h4>
                                    <p>Le nom actuel de cet endroit est la Cour des Myrtes en raison de la présence de buissons de myrtes autour de l'étang central et de la couleur verte vive qui contraste avec le sol en marbre blanc du patio.
                                        Il s'agit d'une longue cour qui entoure le Bassin de la Alberca. A l'époque, les chambres sur les côtés étaient utilisées comme résidence pour les femmes. De plus, dans la galerie Nord, on peut lire un célèbre poème d'Ibn Zamrak en l'honneur de Muhammad V après la conquête d'Algésiras en 1368.
                                    </p>
