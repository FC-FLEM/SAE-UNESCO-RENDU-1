                                    <h4><b>Cour des Lions</b></h4>
                                    <p>La cour des Lions est sans doute l'endroit le plus connu de l'Alhambra. La fontaine au milieu du patio a donné son nom à l'ensemble.
                                        En effet, elle est composée de douze lions-jets d'eau et repose sur un grand bassin en forme dodécagonale en marbre blanc. Cette fontaine est considérée comme l'un des exemples les plus importants de la sculpture musulmane.
                                        La galerie de la Cour de Lions est un espace couvert soutenu par une centaine de colonnes en marbre blanc. Depuis 1984, c'est un lieu faisant partie du patrimoine mondial de l'UNESCO.</p>
