                            <h4>Les Bains</h4>
                            <p>Les Bains de l'Alhambra sont de deux types : le bain pour les hommes et celui pour les femmes. Chacun d'entre eux est composé de plusieurs pièces, y compris une salle de bains chaude ou à vapeur : “Caldarium”, une salle de bains froide : “Frigidarium” et une salle principale : "Tepidarium". Les bains étaient également équipés de fontaines et de bassins pour l'eau chaude et froide.</p>
                            <p>La décoration des Bains de l'Alhambra est riche et sophistiquée, avec de nombreux détails ornementaux tels que des mosaïques, des motifs géométriques, des arabesques et des inscriptions en arabe. Les bains étaient également équipés de lampes à huile et de grands miroirs en verre pour diffuser la lumière naturelle.</p>
