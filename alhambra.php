        <h3>Histoire de l'Alhambra</h3>
            <div class="Histoire">
            <img class="histoire" src="./img/Grenade/Night_view_Alhambra.jpg" float="left">
            <p>L'Alhambra prend place sur la colline Sabika durant le <b>IXe</b> siècle. C'est un ancien premier ministre du nom de Samuel ibn Nagrella qui a reconstruit les bâtiments abandonnés sur cette colline et y a installé son palais. 
            </p><p>Au <b>XIIe</b> siècle, ces bâtiments servent de refuge lors de combats dans Alcazaba del Albaicín.
            </p><p>C'est en <b>1238</b> que le fondateur de la dynastie nasride, Al-Ahmar, s'intalle dans l'ancienne Alcazaba de l'Albaicín. Il prend la décision de reconstruire les ruines de la colline, construisant ainsi l'Alhambra actuelle. Ce fut un palais, une citadelle et une forteresse, mais avant tout, une résidence des haut placés nasrides. Grenade devient alors la capitale du royaume nasride, s'aggrandissant au cours du temps pour laisser place à de nouveaux quartiers jusqu'à la fin du XVe siècle.
            </p><p>Après <b>1492</b>, l'Alhambra est devenue une Maison Royale. 
            </p><p>L'empereur Charles V décide, en <b>1526</b>, de construire son palais éponyme, ainsi que d'autres constructions ayant pour style la Renaissance Romaine.
            </p><p>Au <b>XIXe</b> siècle, la société s'intéresse grandement aux jardins de l'Alhambra. Suite à la révolution de 1868, l'Alhambra est détachée de la Couronne et devient le domaine de l'État, se déclarant en 1870 "monument national". <br>Actuellement, la garde de l'Alhambra est confiée à la Communauté autonome d'Andalousie des fonctions et des services de l'État en matière de culture.</p>
            <br>
            </div>  

        <h3>Arts & Architectures</h3>
            <div class="ART">
            <img class="arte" src="./img/Grenade/Arquitectura_Alhambra.jpg" float="left">
            <p>C'est au XIVe siècle que l'Alhambra s'embellit avec des decorations aux multiples symboles musulmans, notament par Yusuf I. On y administrait la justice de Grenade dans la Puerta de la Ley Islamica, ce qui fait de l'Alhambra plus qu'un simple batiment défensif ou décoratif. Son architecture défensive fait de l'Alhambra une forteresse (remparts, portes fortifiées, tours de gardes) à l'ésthétique "orientale" qui était utilisées par les Emires qui résidaient dans le Generalife, situé dans l'Alhamlbra. <br>On remarque que les derniers batiments possèdent de nombreuses arches rondes et des toits en dôme, afin de laisser entrer le plus de lumière, thème imporant dans l'architecture musulmane.</p>
            <p>Grenade répond au modèle historique d'une ville-colline, entourée de montagnes et arrosée par les fleuves Darro et Genil.
                Entourée de montagnes, les écrivains arabes ont comparé Grenade à une couronne, sur le front de laquelle se dresse le <i>"diadème de l'Alhambra"</i>.</p>
