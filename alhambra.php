                            <h4>Alcabaza</h4>
                            <p>L'Alcazaba est considérée comme la partie la plus ancienne de l'Alhambra. En effet, c'est au début du IXe siecle, qu'on rapporte des constructions sur la colline de la Sabika. C'est, par la suite, à cet endroit précis, que la majestueuse Alhambra prendra place.
                                A l'origine, c'était une forteresse conçue dans l'unique but de protéger la ville de Grenade des invasions ennemies.
                            </p>
                            <p>Le bâtiment est divisé en deux parties : la partie supérieure et la partie inférieure. La partie supérieure ou citadelle était utilisée comme résidence pour les souverains maures et leur cour tandis que la partie inférieure servait de quartier militaire. </p>
                            <p>L'Alcazaba a été restaurée à plusieurs reprises au fil des siècles, notamment par les Chrétiens, et a servi de prison d'État à différentes époques, y compris pendant l'occupation franquiste. </p>
                        </div>
